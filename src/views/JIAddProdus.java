    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String pretTVA = JOptionPane.showInputDialog(this, "Introduceti pretul produsului cu TVA");
        String tva = (String) tvaCombo.getSelectedItem();
        float f = 0.0f;
        float pTva = 0.0f;
        switch (tva) {
            case "20%":
                f = 0.20f;
                break;
            case "9%":
                f = 0.09f;
                break;
            case "5%":
                f = 0.05f;
                break;
            case "0%":
                f = 0f;
                break;
        }
        if (!pretTVA.isEmpty()) {
            if (pretTVA.matches("^[0-9]+([.][0-9]{1,4})?")) {
                try {
                    pTva = Float.parseFloat(pretTVA);
                } catch (IllegalArgumentException e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Introduceti un numar valid folosing . ca despartitor de decimale");
            }
            pretUnitarTxt.setText(String.valueOf(df.format(pTva - pTva * f)));
        }
    }//GEN-LAST:event_jButton3ActionPerformed


