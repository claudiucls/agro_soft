    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String tva = (String) tvaCombo.getSelectedItem();
        String s = pretUnitarTxt.getText();
        double nr = Formatare.formatStringAsDouble(s);
        double pret = 0;
        if(nr!=-1){
             pret=nr; 
        } else {
            JOptionPane.showMessageDialog(null, "Pretul introdus nu este un numar zecimal corect!");
            return;
        }
        Produs p = new Produs(dq.getLastProdusId(), denumireTxt.getText(), umTxt.getText(), tva, pret, Double.parseDouble(cantiatatetxt.getText()), 0);
        List<Produs> lista = mf.jinir.listap;
        for (int i = 0; i < lista.size(); i++) {
            if (lista.get(i).equals(p)) {
                lista.set(i, p);
                mf.jinir.refreshTableProduse(mf.jinir.listap);
                clearText();
                this.hide();
                return;
            }
        }
        mf.jinir.listap.add(p);
        mf.jinir.refreshTableProduse(mf.jinir.listap);
        clearText();
        this.hide();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        String denumire = (String) jTable1.getModel().getValueAt(row, 1);
        double pret = (double) jTable1.getModel().getValueAt(row, 4);
        Produs p = dq.getProdus(denumire, pret);
        denumireTxt.setText(p.getDenumire());
        pretUnitarTxt.setText(String.valueOf(p.getPretUnitar()));
        cantiatatetxt.setText(String.valueOf(p.getStoc()));
        umTxt.setText(p.getUm());
        //       jTextField6.setText(p.getTva());
        String d = p.getTva();
        if (d.equalsIgnoreCase("20%")) {
            tvaCombo.setSelectedIndex(0);
        }
        if (d.equalsIgnoreCase("9%")) {
            tvaCombo.setSelectedIndex(1);
        }
        if (d.equalsIgnoreCase("5%")) {
            tvaCombo.setSelectedIndex(2);
        }
        if (d.equalsIgnoreCase("0%")) {
            tvaCombo.setSelectedIndex(3);
        }
    }//GEN-LAST:event_jTable1MouseClicked

