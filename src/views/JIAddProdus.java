    public void resizeColumnWidth(JTable table) {
        final TableColumnModel columnModel = table.getColumnModel();
        for (int column = 0; column < table.getColumnCount(); column++) {
            int width = 50; // Min width
            for (int row = 0; row < table.getRowCount(); row++) {
                TableCellRenderer renderer = table.getCellRenderer(row, column);
                Component comp = table.prepareRenderer(renderer, row, column);
                width = Math.max(comp.getPreferredSize().width + 1, width);
            }
            columnModel.getColumn(column).setPreferredWidth(width);
        }
    }

    public void refreshTable(String cauta) {
        List<Produs> produse = dq.getProduse(cauta);
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; //To change body of generated methods, choose Tools | Templates.
            }

        };
        model.setColumnIdentifiers(new Object[]{"Cod", "Denumire", "TVA", "UM", "Pret Unitar", "Stoc", "Valoare"});
        Object[] row = new Object[7];
        for (int i = 0; i < produse.size(); i++) {
            row[0] = i + 1;
            row[1] = produse.get(i).getDenumire();
            row[2] = produse.get(i).getTva();
            row[3] = produse.get(i).getUm();
            row[4] = produse.get(i).getPretUnitar();
            row[5] = produse.get(i).getStoc();
            row[6] = produse.get(i).getValoare();
            model.addRow(row);
        }
        jTable1.setModel(model);

    }

