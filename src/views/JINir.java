    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(listap.isEmpty()){
             JOptionPane.showMessageDialog(null, "NU sunt produse in NIR. Adaugati Produse", "EROARE NIR!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!dq.addNir(nir)) {
            JOptionPane.showMessageDialog(null, "A aparut o erroare la crearea NIR-ului", "EROARE NIR!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        nir.setId(dq.getLastNir());
        for (Produs p : listap) {
            p.setIntrare(nir.getId());
            if (!dq.addProdusNou(p)) {
                JOptionPane.showMessageDialog(null, "A aparut o erroare la salvarea Produsului " + p.getDenumire(), "Erroare: Salvare Produs in nir!", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        Connection conn = DBConnect.getConnection();
      
        Integer nrNir = nir.getNrNir();
        String data =  new SimpleDateFormat("yyyy-MM-dd").format(java.sql.Date.valueOf(nir.getDate()));
        Map<String,Object> params = new HashMap<>();
        params.put("numarul_nir", nrNir);
        params.put("data", data);
        String absolutePath = "D:\\Ags_Soft\\Program Sursa\\rapoarte\\";
        params.put("SUBREPORT_DIR", absolutePath);
       
        try {
            InputStream in = getClass().getResourceAsStream("/rapoarte/nirladata.jasper");
            JasperPrint jp = JasperFillManager.fillReport(in, params, conn);
            if(jp.getPages().isEmpty()){
                JOptionPane.showMessageDialog(null, "Nu sunt nir-uri de afisat!");
                return;
            } else {
                JRViewer viewer1 = new JRViewer(jp);
                JFrame jf = new JFrame();
                jf.setLocation(0, 0);
                Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
                jf.setSize(dim);
                jf.add(viewer1);
                jf.setVisible(true);
                jf.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            }
            

        } catch (JRException ex) {
            Logger.getLogger(JIListareNiruri.class.getName()).log(Level.SEVERE, null, ex);
        }
        clear();
        this.hide();
    }//GEN-LAST:event_jButton3ActionPerformed

