    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Selectati sau introduceti o data de forma 'zz-ll-aaaa'", "Data goala sau gresita!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(dq.getLastNirDate()!=null && jDateChooser1.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate().isBefore(dq.getLastNirDate())){
             JOptionPane.showMessageDialog(null, "Exista o intrare cu o data anterioara acesteia", "Data goala sau gresita!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jNrDocTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Introduceti numarul documentului ( nr. Factura sau nr. Bon Fiscal) ", "Nr document gol!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jFurnizoriList.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Selectati un furnizor din lista sau adaugati unul nou, apoi selectatiti-l", "Furnizor neselectat!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jGestiuniList.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Selectati o gestiune din lista", "Gestiune neselectat!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String tipDoc = "";
        if (tipDocBf.isSelected()) {
            tipDoc = "BF";
        }
        if (tipDocFf.isSelected()) {
            tipDoc = "FF";
        }

        nir = new Nir(dq.getLastNir(),
                Integer.parseInt(nrNirTxt.getText()),
                jDateChooser1.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate(),
                tipDoc,
                jNrDocTxt.getText(),
                jFurnizoriList.getSelectedValue(),
                dq.getGestiuneId(jGestiuniList.getSelectedValue()));

        if (!jiad.isShowing()) {
            jiad.show();
        } else {
            jiad.hide();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

