    public void refreshTableProduse(List<Produs> listp) {
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        };
        model.setColumnIdentifiers(new Object[]{"Nr.Crt.", "Denumire", "UM", "Pret Unitar", "Cantitate", "Valoare"});

        Object[] row = new Object[6];
        for (int i = 0; i < listap.size(); i++) {
            row[0] = i + 1;
            row[1] = listap.get(i).getDenumire();
            row[2] = listap.get(i).getUm();
            row[3] = listap.get(i).getPretUnitar();
            row[4] = listap.get(i).getCantitateIesire();
            row[5] = df.format(listap.get(i).getPretUnitar() * listap.get(i).getCantitateIesire());
            model.addRow(row);
        }
        jTable1.setModel(model);
        dq.resizeColumnWidth(jTable1);
    }

