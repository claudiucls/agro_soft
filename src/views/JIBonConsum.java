    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Selectati sau introduceti o data de forma 'zz-ll-aaaa'", "Data goala sau gresita!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (dq.getLastBCDate() != null && jDateChooser1.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate().isBefore(dq.getLastBCDate())) {
            JOptionPane.showMessageDialog(null, "Exista un bon de consum cu o data anterioara acesteia", "Data goala sau gresita!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jGestiuneList.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Selectati o Gestiune din lista ", "Gestiune neselectata!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jComandaList.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Selectati o Comanda din lista ", "Comanda neselectata!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        bc = new BonConsum(dq.getLastBCId(),
                Integer.parseInt(nrConsum.getText()),
                jDateChooser1.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate(),
                jComandaList.getSelectedValue(),
                dq.getGestiuneId(jGestiuneList.getSelectedValue()));

        int gestiune = dq.getGestiuneId(jGestiuneList.getSelectedValue());
        if (!jiadb.isShowing()) {
            jiadb.gest = gestiune;
            jiadb.refreshTable(gestiune, "");
            jiadb.show();
        } else {
            jiadb.hide();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (listap.isEmpty()) {
            JOptionPane.showMessageDialog(null, "NU sunt produse in Bonul de Consum! Adaugati Produse!", "EROARE Bon!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!dq.addBon(bc)) {
            JOptionPane.showMessageDialog(null, "A aparut o erroare la crearea Bonului De Consum", "EROARE Bon!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        bc.setId(dq.getLastBCId());
        for (Produs p : listap) {
            p.setIesire(bc.getId());
            if (!dq.addProdusInBon(p)) {
                JOptionPane.showMessageDialog(null, "A aparut o erroare la salvarea Produsului " + p.getDenumire(), "Erroare: Salvare Produs in Bon!", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        Connection conn = DBConnect.getConnection();

        Integer nrBon = bc.getNrBon();
        String data = new SimpleDateFormat("yyyy-MM-dd").format(java.sql.Date.valueOf(bc.getData()));
        Map<String, Object> params = new HashMap<>();
        params.put("nrBon", nrBon);
        params.put("data", data);
        String absolutePath = "D:\\Ags_Soft\\Program Sursa\\rapoarte\\";
        params.put("SUBREPORT_DIR", absolutePath);

        try {
            InputStream in = getClass().getResourceAsStream("/rapoarte/bcSimplu.jasper");
            JasperPrint jp = JasperFillManager.fillReport(in, params, conn);
            if (jp.getPages().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nu sunt Bonuri de consum de afisat!");
                return;
            } else {
                JRViewer viewer1 = new JRViewer(jp);
                JFrame jf = new JFrame();
                jf.setLocation(0, 0);
                Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
                jf.setSize(dim);
                jf.add(viewer1);
                jf.setVisible(true);
                jf.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            }

        } catch (JRException ex) {
            Logger.getLogger(JIListareNiruri.class.getName()).log(Level.SEVERE, null, ex);
        }
        clear();
        this.hide();
    }//GEN-LAST:event_jButton3ActionPerformed

