    public void refreshTableProduse(List<Produs> listp) {
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                //all cells false
                return false;
            }
        };
        model.setColumnIdentifiers(new Object[]{"Nr.Crt.", "Denumire", "UM","TVA", "Pret Unitar", "Cantitate", "Valoare","Valaore cu TVA"});

        Object[] row = new Object[8];
        for (int i = 0; i < listap.size(); i++) {
            row[0] = i + 1;
            row[1] = listap.get(i).getDenumire();
            row[2] = listap.get(i).getUm();
            row[3]=  listap.get(i).getTva();
            row[4] = listap.get(i).getPretUnitar();
            row[5] = listap.get(i).getCantitateFacturata();
            row[6] = df.format(listap.get(i).getPretUnitar() * listap.get(i).getCantitateFacturata());
            row[7] = df.format((listap.get(i).getPretUnitar() * listap.get(i).getCantitateFacturata())+(listap.get(i).getPretUnitar() * listap.get(i).getCantitateFacturata()*returnTva(listap.get(i).getTva())));
            model.addRow(row);
        }
        jTable1.setModel(model);
        dq.resizeColumnWidth(jTable1);
    }

