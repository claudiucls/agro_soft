    private double returnTva(String tva){
        double d =0;
        switch(tva){
            case "20%": d=0.20; break;
            case "9%": d=0.09; break;
            case "5%": d=0.05; break;
        }
        return d;
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Selectati sau introduceti o data de forma 'zz-ll-aaaa'", "Data goala sau gresita!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (nrFact.getText().isEmpty() || dq.existaFacturaCuNr(Integer.parseInt(nrFact.getText()))==true) {
            JOptionPane.showMessageDialog(null, "Nu ati introdus numarul facturii sau exista o factura cu acest numar in baza de date", "Nr factura gresit!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jGestiuneList.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Selectati o Gestiune din lista ", "Gestiune neselectata!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jClientiList.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Selectati un Client din lista ", "Client neselectat!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        factura = new Factura(dq.getLastFacturaId()+1,
                Integer.parseInt(nrFact.getText()),
                jDateChooser1.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate(),
                jClientiList.getSelectedValue(),
                dq.getGestiuneId(jGestiuneList.getSelectedValue()));

        int gestiune = dq.getGestiuneId(jGestiuneList.getSelectedValue());
        if (!jiaf.isShowing()) {
            jiaf.gest = gestiune;
            jiaf.refreshTable(gestiune, "");
            jiaf.show();
        } else {
            jiaf.hide();
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    private void verifica(){
        if (listap.isEmpty()) {
            JOptionPane.showMessageDialog(null, "NU sunt produse in Factura! Adaugati Produse!", "EROARE creare factura!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        // pentru delegat 
        factura.setDelegNume(delegNumeTxt.getText());
        factura.setDelegCi(delegCiTxt.getText());
        factura.setDelegTrans(delegTransTxt.getText());
        if(scadentaTxt.getText().isEmpty()&& !(scadentaTxt.getText().matches("[0-9]+"))){
            factura.setZileScad(0);
        } else {
            factura.setZileScad(Integer.parseInt(scadentaTxt.getText()));
        }
        if(chitantaTxt.getText().isEmpty()&& !(chitantaTxt.getText().matches("[0-9]+"))){
            factura.setChitanta(null);
        } else {
            factura.setChitanta(chitantaTxt.getText());
        }
        //salvare factura
         if (!dq.addFactura(factura)) {
            JOptionPane.showMessageDialog(null, "A aparut o erroare la crearea facturii", "EROARE creare factura!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        factura.setId(dq.getLastFacturaId());
        
    }

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        verifica();
        for (Produs p : listap) {
            p.setFactura(factura.getId());
            if (!dq.addProdusInFactura(p)) {
                JOptionPane.showMessageDialog(null, "A aparut o erroare la salvarea Produsului " + p.getDenumire(), "Erroare: Salvare Produs in Factura!", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        double totalF = getTotalFactura(listap);
        factura.setTotal(totalF);
        if (!dq.updateFactura(factura)) {
                JOptionPane.showMessageDialog(null, "A aparut o erroare la salvarea facturii", "Erroare: Actualizare in Factura!", JOptionPane.ERROR_MESSAGE);
                return;
            }
        Integer nrFact =factura.getNrFact();
        String data = new SimpleDateFormat("yyyy-MM-dd").format(java.sql.Date.valueOf(factura.getData()));
        String scadenta = new SimpleDateFormat("dd-MM-yyyy").format(java.sql.Date.valueOf(factura.getData().plusDays(factura.getZileScad())));
        System.out.println(scadenta);
        Map<String, Object> params = new HashMap<>();
        params.put("nrFact", nrFact);
        params.put("data", data);
        params.put("delegNume",factura.getDelegNume());
        params.put("delegCI",factura.getDelegCi());
        params.put("delegTrans",factura.getDelegTrans());
        String absolutePath = "D:\\Ags_Soft\\Program Sursa\\rapoarte\\";
        params.put("SUBREPORT_DIR", absolutePath);
        Connection conn = DBConnect.getConnection();
        try {
            InputStream in = getClass().getResourceAsStream("/rapoarte/facturaSimpla.jasper");
            JasperPrint jp = JasperFillManager.fillReport(in, params, conn);
            if (jp.getPages().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nu este nimic de afisat!");
                return;
            } else {
                JRViewer viewer1 = new JRViewer(jp);
                JFrame jf = new JFrame();
                jf.setLocation(0, 0);
                Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
                jf.setSize(dim);
                jf.add(viewer1);
                jf.setVisible(true);
                jf.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            }

        } catch (JRException ex) {
            Logger.getLogger(JIListareNiruri.class.getName()).log(Level.SEVERE, null, ex);
        }
        clear();
        this.hide();
    }//GEN-LAST:event_jButton3ActionPerformed

