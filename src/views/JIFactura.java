    private double getTotalFactura(List<Produs> lista){
        double total = 0;
        double totaltva=0;
        for (Produs p : lista) {
           total+= p.getCantitateFacturata()*p.getPretUnitar();
           totaltva +=p.getCantitateFacturata()*p.getPretUnitar()*returnTva(p.getTva());
        }
        return total+totaltva;
    }
    
